import { create } from "zustand";

type User = { email: string } | null;

type AuthState = {
  user: User;
  login: (email: string) => void;
  logout: () => void;
};

export const useAuthStore = create<AuthState>((set) => ({
  user: null,
  login: (email) => set({ user: { email } }),
  logout: () => set({ user: null }),
}));
